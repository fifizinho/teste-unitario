package com.teste.calculos;

public class Calculadora {
	public static double sum(double valor1, double valor2) {
		return valor1 + valor2;
	}
	public static double menos(double valor1, double valor2) {
		return valor1 - valor2;
	}
	public static double multiplicacao(double valor1, double valor2) {
		return valor1 * valor2;
	}
	public static double div(double valor1, double valor2) {
		return valor1 / valor2;
	}
}
